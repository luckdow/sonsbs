// Hreflang mapping for SEO
export const hreflangMapping = {
  'tr': 'tr-TR',
  'en': 'en-US', 
  'de': 'de-DE',
  'ru': 'ru-RU',
  'ar': 'ar-SA'
};

// Language specific URL structures
export const languageUrlStructures = {
  tr: '', // Default language, no prefix
  en: '/en',
  de: '/de', 
  ru: '/ru',
  ar: '/ar'
};

// Generate keywords for specific language and page
export const generateMultilingualKeywords = (language, pageType, location) => {
  const langData = multilingualKeywords[language];
  if (!langData) return '';
  
  if (pageType === 'city' && langData.cities[location]) {
    const cityData = langData.cities[location];
    return [cityData.primary, cityData.secondary, cityData.longTail, cityData.local]
      .filter(Boolean)
      .join(', ');
  }
  
  if (pageType === 'service' && langData.services[location]) {
    return langData.services[location];
  }
  
  return '';
};

// Generate hreflang tags for a page
export const generateHreflangTags = (currentPath, availableLanguages = ['tr', 'en', 'de', 'ru', 'ar']) => {
  return availableLanguages.map(lang => {
    const baseUrl = 'https://gatetransfer.com';
    const langPrefix = languageUrlStructures[lang];
    const href = `${baseUrl}${langPrefix}${currentPath}`;
    
    return {
      rel: 'alternate',
      hreflang: hreflangMapping[lang],
      href: href
    };
  });
};

export default multilingualKeywords;
