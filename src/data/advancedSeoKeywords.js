export const generateKeywordDensityReport = (content, keywords) => {
  const text = content.toLowerCase();
  const wordCount = text.split(/\s+/).length;
  
  return keywords.map(keyword => {
    const keywordLower = keyword.toLowerCase();
    const occurrences = (text.match(new RegExp(keywordLower, 'g')) || []).length;
    const density = ((occurrences / wordCount) * 100).toFixed(2);
    
    return {
      keyword,
      occurrences,
      density: parseFloat(density),
      status: density >= 0.5 && density <= 2.5 ? 'optimal' : 
              density < 0.5 ? 'low' : 'high',
      recommendation: density < 0.5 ? 'Anahtar kelimeyi daha fazla kullanın' :
                     density > 2.5 ? 'Anahtar kelime kullanımını azaltın' :
                     'Optimal seviyede'
    };
  });
};

export const suggestRelatedKeywords = (mainKeyword, language = 'tr') => {
  const allKeywords = [
    ...advancedSeoKeywords.primary[language] || [],
    ...advancedSeoKeywords.longTail[language] || [],
    ...advancedSeoKeywords.questionBased[language] || []
  ];
  
  return allKeywords
    .filter(item => item.keyword.includes(mainKeyword.toLowerCase()) || 
                   mainKeyword.toLowerCase().includes(item.keyword))
    .sort((a, b) => b.volume - a.volume)
    .slice(0, 10);
};

